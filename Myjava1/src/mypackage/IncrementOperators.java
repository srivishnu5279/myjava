package mypackage;

public class IncrementOperators {

	public static void main(String[] args) {
		int a=10;
		//post increment
		System.out.println("a++ = " + a++);

		System.out.println("after increment" + a);
		
		//preincrement
		System.out.println("++a = " + ++a);
			
		//postdecrement
			System.out.println("a-- = " + a--);

			System.out.println("after postdecrement " + a);
			
		//predecremnt
			System.out.println("--a = " + --a);
				

	}

}
